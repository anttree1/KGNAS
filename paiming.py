def compare_elements(list1):
    list2 = []
    combinations = itertools.combinations(list1, 2)
    for pair in combinations:
        if pair[0] > pair[1]:
            list2.append(1)
        else:
            list2.append(0)
    return list2


def compare_and_count(list1, list2):
    if len(list1) != len(list2):
        raise ValueError("Both lists should have the same length.")
    list3 = [0] * len(list1)
    mismatch_count = 0
    for index, (item1, item2) in enumerate(zip(list1, list2)):
        if item1 != item2:
            mismatch_count += 1
        list3[index] = mismatch_count
    return list3


